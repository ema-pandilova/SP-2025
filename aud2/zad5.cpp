// Да се напише програма која ќе ги отпечати на екран остатоците при делењето на бројот 19 со 2, 3, 5 и 8.

#include <iostream>
using namespace std;

int main() {
    int a;
    cin >> a;
    cout << a << " % " << 2 << " = " << a % 2 << endl;
    cout << a << " % " << 3 << " = " << a % 3 << endl;
    cout << a << " % " << 4 << " = " << a % 4 << endl;
    cout << a << " % " << 5 << " = " << a % 5 << endl;
    cout << a << " % " << 6 << " = " << a % 6 << endl;
    return 0;
}






